package com.neixar.sct.buscaLigas.Record;

import java.util.ArrayList;

public class Registro {
	
	private ArrayList<String> urls;
	private ArrayList<String> archivos;
	private ArrayList<String> ejemplos;
	
	
	public ArrayList<String> getUrl() {
		return urls;
	}
	
	public void setUrls(ArrayList<String> urls) {
		this.urls = urls;
	}
	
	public ArrayList<String> getArchivos() {
		return archivos;
	}
	
	public void setArchivos(ArrayList<String> archivos) {
		this.archivos = archivos;
	}
	
	public ArrayList<String> getEjemplos() {
		return ejemplos;
	}
	
	public void setEjemplos(ArrayList<String> ejemplos) {
		this.ejemplos = ejemplos;
	}
	
	

}
